
public class Operations {

	public static void main(String[] args)
	{
		int a=5;
		int b=6;
		String cha="+";
		switch(cha)
		{
		case "+":System.out.println(a+b);
		break;
		case "*":System.out.println(a*b);
		break;
		case "-":System.out.println(a-b);
		break;
		case "/":System.out.println(a/b);
		break;
		default: System.out.println("wrong input");
		}				
		
	}

}
